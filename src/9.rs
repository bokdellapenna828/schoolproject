// A simple function that takes two arguments and returns their sum
fn sum(a: i32, b: i32) -> i32 {
    a + b
}

// A simple function that takes two arguments and returns their product
fn product(a: i32, b: i32) -> i32 {
    a * b
}

// A simple function that takes one argument and returns its square
fn square(a: i32) -> i32 {
    a * a
}

// A simple function that takes two arguments and returns their difference
fn difference(a: i32, b: i32) -> i32 {
    a - b
}
